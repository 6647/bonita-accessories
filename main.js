// Set up filter button functionality
function setupFilterButtons() {
    const filterButtons = document.querySelectorAll('.filter-btn');

    filterButtons.forEach(button => {
        button.addEventListener('click', function () {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');

            const category = this.dataset.category;
            filterProducts(category);
        });
    });
}

// Filter products by category
function filterProducts(category) {
    if (category === 'all') {
        renderProducts(products);
        return;
    }
    
    const filteredProducts = products.filter(product => 
        product.categories.includes(category)
    );
    renderProducts(filteredProducts);
}

// Handle WhatsApp contact for a product
function contactAboutProduct(productId) {
    const product = products.find(p => p.id === productId);
    if (!product) return;

    const message = `Hello Bonita Accessories! I'm interested in purchasing your ${product.title} (${product.price}). Please provide more details.`;
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${encodedMessage}`;

    window.open(whatsappUrl, '_blank');
}