// WhatsApp contact number
const whatsappNumber = "966501234567";

// Initialize the page
document.addEventListener('DOMContentLoaded', function () {
    // Show splash screen for 3 seconds
    setTimeout(function () {
        const splashScreen = document.getElementById('splashScreen');
        const mainContent = document.getElementById('mainContent');

        splashScreen.style.opacity = '0';
        splashScreen.style.visibility = 'hidden';

        mainContent.style.opacity = '1';

        renderProducts(products);
        setupFilterButtons();
    }, 100);

});

// Render products to the page
function renderProducts(productsToRender) {
    const productsContainer = document.getElementById('productsContainer');
    
    if (productsToRender.length === 0) {
        productsContainer.innerHTML = '<div class="no-products">No products found in this category.</div>';
        return;
    }
    
    let html = '';
    
    productsToRender.forEach(product => {
        // Convert categories array to space-separated string for data attribute
        const categoryClasses = product.categories.join(' ');
        
        html += `
            <div class="product-card" data-categories="${categoryClasses}">
                <div class="product-image-container">
                    <img src="${product.image}" alt="${product.title}" class="product-image" loading="lazy">
                </div>
                <div class="product-info">
                    <h3 class="product-title">${product.title}</h3>
                    <p class="product-title">منتج رقم ${product.id}</hp>
                    <h3 class="product-description">${product.description}</h3>
                    <div class="product-price">${product.price}</div>
                    <button class="whatsapp-btn" onclick="contactAboutProduct(${product.id})">
                        <span class="whatsapp-icon">📱</span> Order via WhatsApp
                    </button>
                </div>
            </div>
        `;
    });
    
    productsContainer.innerHTML = html;
}

// Set up filter button functionality
function setupFilterButtons() {
    const filterButtons = document.querySelectorAll('.filter-btn');

    filterButtons.forEach(button => {
        button.addEventListener('click', function () {
            filterButtons.forEach(btn => btn.classList.remove('active'));
            this.classList.add('active');

            const category = this.dataset.category;
            filterProducts(category);
        });
    });
}

// Filter products by category
function filterProducts(category) {
    if (category === 'all') {
        renderProducts(products);
        return;
    }
    
    const filteredProducts = products.filter(product => 
        product.categories.includes(category)
    );
    renderProducts(filteredProducts);
}

// Handle WhatsApp contact for a product
function contactAboutProduct(productId) {
    const product = products.find(p => p.id === productId);
    if (!product) return;

    const message = `Hello Bonita Accessories! I'm interested in purchasing your ${product.title} (${product.price}). Please provide more details.`;
    const encodedMessage = encodeURIComponent(message);
    const whatsappUrl = `https://wa.me/${whatsappNumber}?text=${encodedMessage}`;

    window.open(whatsappUrl, '_blank');
}